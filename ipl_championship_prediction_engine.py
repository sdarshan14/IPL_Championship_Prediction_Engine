import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import os

# =====================================================
# CREATE FOLDERS
# =====================================================

os.makedirs("charts", exist_ok=True)
os.makedirs("output", exist_ok=True)

# =====================================================
# LOAD EXCEL
# =====================================================

file_name = "IPL_Championship_Research_Dataset.xlsx"

raw = pd.read_excel(file_name)

print("Original Shape:", raw.shape)

# =====================================================
# HANDLE BOTH DATASET FORMATS
# =====================================================

if raw.shape[1] == 1:

    print("Single-column dataset detected")

    col = raw.columns[0]

    raw = raw.dropna()

    df = raw[col].astype(str).str.split(",", expand=True)

    df.columns = [
        "Season",
        "Team",
        "LeaguePosition",
        "Matches",
        "Wins",
        "Losses",
        "NoResult",
        "Points",
        "NRR",
        "C",
        "RU"
    ]

else:

    print("Multi-column dataset detected")

    df = raw.copy()

# =====================================================
# CLEAN COLUMN NAMES
# =====================================================

df.columns = [str(c).strip() for c in df.columns]

# =====================================================
# NUMERIC CONVERSION
# =====================================================

numeric_cols = [
    "Season",
    "LeaguePosition",
    "Matches",
    "Wins",
    "Losses",
    "NoResult",
    "Points",
    "NRR",
    "C",
    "RU"
]

for col in numeric_cols:
    df[col] = pd.to_numeric(df[col], errors="coerce")

df = df.dropna()

# =====================================================
# DATASET INFO
# =====================================================

print("\nDataset Loaded Successfully\n")

print(df.head())

print("\nShape:", df.shape)

print("\nColumns:\n", df.columns.tolist())

# =====================================================
# SAVE CLEAN DATASET
# =====================================================

df.to_csv(
    "output/cleaned_ipl_dataset.csv",
    index=False
)

# =====================================================
# CHAMPIONS DATA
# =====================================================

champions = df[df["C"] == 1]

runnerups = df[df["RU"] == 1]

# =====================================================
# TITLES BY POSITION
# =====================================================

titles_by_position = (
    champions
    .groupby("LeaguePosition")
    .size()
)

print("\nTitles By Position\n")

print(titles_by_position)

plt.figure(figsize=(8,5))

titles_by_position.plot(
    kind="bar"
)

plt.title("IPL Titles by League Position")

plt.xlabel("League Position")

plt.ylabel("Titles")

plt.tight_layout()

plt.savefig(
    "charts/titles_by_position.png"
)

plt.show()

# =====================================================
# RUNNER UPS BY POSITION
# =====================================================

runnerups_by_position = (
    runnerups
    .groupby("LeaguePosition")
    .size()
)

plt.figure(figsize=(8,5))

runnerups_by_position.plot(
    kind="bar"
)

plt.title("Runner-Ups by League Position")

plt.xlabel("League Position")

plt.ylabel("Runner-Ups")

plt.tight_layout()

plt.savefig(
    "charts/runnerups_by_position.png"
)

plt.show()

# =====================================================
# TOP 2 ANALYSIS
# =====================================================

top2_titles = len(
    champions[
        champions["LeaguePosition"] <= 2
    ]
)

total_titles = len(champions)

top2_percentage = (
    top2_titles /
    total_titles
) * 100

print(
    f"\nTop 2 Teams Won {top2_percentage:.2f}% of Titles"
)

# =====================================================
# POSITION SUCCESS RATE
# =====================================================

position_total = (
    df["LeaguePosition"]
    .value_counts()
    .sort_index()
)

position_titles = (
    champions["LeaguePosition"]
    .value_counts()
    .sort_index()
)

success_rate = (
    position_titles /
    position_total
) * 100

print("\nPosition Success Rate (%)\n")

print(success_rate.round(2))

# =====================================================
# CHAMPIONSHIP READINESS INDEX
# =====================================================

df["CRI"] = (

    (5 - df["LeaguePosition"]) * 20

    +

    (df["Points"] * 2)

    +

    (df["NRR"] * 20)

)

# =====================================================
# TOP CRI TEAMS
# =====================================================

top_cri = (
    df
    .sort_values(
        "CRI",
        ascending=False
    )
    .head(20)
)

plt.figure(figsize=(12,8))

sns.barplot(
    x="CRI",
    y="Team",
    data=top_cri
)

plt.title(
    "Top 20 Teams by CRI"
)

plt.tight_layout()

plt.savefig(
    "charts/top_cri_teams.png"
)

plt.show()

# =====================================================
# TEAM TITLE COUNT
# =====================================================

team_titles = (
    champions["Team"]
    .value_counts()
)

plt.figure(figsize=(10,6))

sns.barplot(
    x=team_titles.values,
    y=team_titles.index
)

plt.title(
    "IPL Titles by Team"
)

plt.tight_layout()

plt.savefig(
    "charts/team_titles.png"
)

plt.show()

# =====================================================
# NRR ANALYSIS
# =====================================================

plt.figure(figsize=(8,5))

sns.boxplot(
    x="C",
    y="NRR",
    data=df
)

plt.title(
    "Champion vs NRR"
)

plt.tight_layout()

plt.savefig(
    "charts/champion_vs_nrr.png"
)

plt.show()

# =====================================================
# POINTS ANALYSIS
# =====================================================

plt.figure(figsize=(8,5))

sns.boxplot(
    x="C",
    y="Points",
    data=df
)

plt.title(
    "Champion vs Points"
)

plt.tight_layout()

plt.savefig(
    "charts/champion_vs_points.png"
)

plt.show()

# =====================================================
# CORRELATION MATRIX
# =====================================================

corr_df = df[
    [
        "LeaguePosition",
        "Points",
        "NRR",
        "CRI",
        "C"
    ]
]

plt.figure(figsize=(8,6))

sns.heatmap(
    corr_df.corr(),
    annot=True
)

plt.title(
    "Correlation Matrix"
)

plt.tight_layout()

plt.savefig(
    "charts/correlation_matrix.png"
)

plt.show()

# =====================================================
# YEARLY CHAMPIONS
# =====================================================

print("\nYearly Champions\n")

print(
    champions[
        ["Season","Team"]
    ]
)

# =====================================================
# EXPORT FINAL DATA
# =====================================================

df.to_csv(
    "output/ipl_research_output.csv",
    index=False
)

